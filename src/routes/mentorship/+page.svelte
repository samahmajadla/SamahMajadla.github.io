	<h3>Overview</h3>
	<p><a href="/community">Habibi Code</a> hosts a mentorship program with three-month long mentorship cycles for individuals who identify as a
	minority in the tech industry (women, BIPOC, Latinx, LGBTQ+ individuals, formerly incarcerated, low socio-economic backgorund, etc).
	As a participant in this program, you will get one on one time with mentors that can be used in whatever
	ways best suit your needs. Please read through the following information; you can find the application form at the end of the page. </p>
	<h3>Status</h3>
	<p style="color:darkgreen">
		<b><i>Open to applications until September 30, 2022.</i></b> The next mentorship cycle will run from
		November 1, 2022 to January 31, 2022. 
        <br>
        <br>
		<a href="https://forms.gle/wpGW5qiCxjgm5DhE6">Apply here.</a>
        <br>
        <br>
        Please note that due to our limited pool of mentors and the large demand for mentorship, not everyone who applies is guaranteed to receive a mentor. You are welcome to reapply in the future, or join a waiting list to be notified of and considered for any openings or additional mentors. 
	</p>

	<h3>Program</h3>
	<i>You can use this program for whatever best suits your needs, including but not limited to:</i>
	<ul style="padding-left:20%">
		<li>Advice and guidance on education</li>
		<li>Advice and guidance on career</li>
		<li>Resume review</li>
		<li>Brainstorming support (technical or product)</li>
		<li>Interview prep</li>
		<li>Support in learning a new tool or technology</li>
		<li>Reviewing and further exloring computer science and/or software engineering concepts</li>
		<li>
			Troubleshooting, debugging, or code review (note: mentors will help point you in the right direction
			as opposed to providing a solution)
		</li>
	</ul>
    You may request to focus on other topics or ideas during the 1:1 time, however mentors reserve the right
	to decline to focus on any topic for any reason.

	<h3>Qualifications</h3>
	In order to qualify for this opportunity, you must:
	<ul>
		<li>Identify as a minority in the tech industry.</li>
		<li>Have at least a beginners foundation in computer science or software engineering</li>
		<li>
			Be able to commit to meeting for 30 minutes every two weeks, and 45 minutes every month.
		</li>
	</ul>

	<h3>Expectations and further details</h3>

	<p>If you are chosen to be a part of this program, you are expected to commit to the following:</p>

	<ul>
		<li>1:1 meeting every 2 weeks (half hour)</li>
		<li>Panel and Q&A style group meeting every month (45 minutes)</li>
	</ul>

	<p>You will also be invited to join all of our <a href="../community">Habibi Code</a> events. Other
	information:</p>
	<ul>
		<li>We will provide a Zoom link for all meetings</li>
		<li>You will coordinate with your mentor on your meeting times.</li>
		<li>You will have access to private channels in Habibi Code to communicate with your mentor, and a private channel to communicate with all other mentees.</li>
	</ul>

