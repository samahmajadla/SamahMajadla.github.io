Habibi Code is a community of passionate and value-driven technologists aiming to empower, support, and encourage fellow minorities in tech to further their academic or career goals. Our community is a safe space to get to know and learn from others, ask questions, share advice, attend events, and more.

<h3>Events</h3>
Habibi Code puts on events on a variety of topics. Some examples of events include:

<ul>
    <li>Introduction to Relational Databases</li>
    <li>Resume Review Workshop</li>
    <li>Building a Full-Stack Application - Backend and Frontend</li>
    <li>Q&A Panels with Professionals in Tech</li>
</ul>

<h3>Eligibility</h3>

If you identify as a minority in tech, you are welcome to join Habibi Code. This includes, but is not limited to: women, BIPOC, the Latinx community, LGBTQ+ individuals, the formerly incarcerated community, those from low socio-economic backgrounds, those with non-traditional educational backgrounds. 

<h3>Join</h3>
<a href="https://discord.gg/f9JwpyvhqP">Here is a link to join our Discord community!</a> Please note that you will not have full access to Habibi Code once you join - in order to maintain the purposeful environment we are fostering, we ask that anyone interested in joining Habibi Code follow the instructions in the 'welcome-and-rules' channel. If you self-identify as a minority in tech, you will receive access to our full Discord community.  We are also collecting optional demographic information to better understand our community makeup.
