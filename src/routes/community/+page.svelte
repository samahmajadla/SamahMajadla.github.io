Habibi Code is a community of passionate and empathetic technologists intended to empower, support, and encourage minorities in tech to further their academic or career goals. Join our community and get to know and learn from each other; ask questions; share advice, job postings, articles and more. 

<h3>Events</h3>
Habibi Code puts on events every month on a variety of topics (starting October 2022). Some examples of events include:

<ul>
    <li>Database design concepts (1 hr)</li>
    <li>Database design exercises (1 hr)</li>
    <li>From idea to database model (1 hr)</li>
    <li>Build a simple full-stack application (2 hrs)</li>
    <li>Intro to basic Python using Turtle graphics (1.5 hrs)</li>
    <li>Build a simple Android app (2 hrs)</li>
    <li>Tech Panels (Q&A, Myths/Lessons learned in tech, Salary Negotiation, Interview Prep, etc)</li>
</ul>

<h3>Eligibility</h3>

If you identify as a minority in tech, you are welcome to join Habibi Code. This includes, but is not limited to: women, BIPOC, the Latinx community, LGBTQ+ individuals, the formerly incarcerated community. We also welcome those from low socio-economic backgrounds.

<h3>Join</h3>
In order to maintain the purposeful environment we are fostering, we ask that anyone interested in joining this community fill out a form confirming your identity as a minority in tech. If you meet the eligibility criteria, you will receive an invitation to join our Discord group within 2 business days. We are also collecting optional demographic information to better understand our community makeup.
