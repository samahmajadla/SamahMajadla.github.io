Habibi Code is a community of passionate and empathetic technologists intended to empower, support, and encourage fellow minorities in tech to further their academic or career goals. Join our community and get to know and learn from each other; practice interview questions; ask questions; share advice, job postings, articles and more. 

<h3>Events</h3>
Habibi Code puts on events every month on a variety of topics (starting November 2022). Some examples of events include:

<ul>
    <li>Database design concepts (1 hr)</li>
    <li>Database design exercises (1 hr)</li>
    <li>From idea to database model (1 hr)</li>
    <li>Build a simple full-stack application (2 hrs)</li>
    <li>Front-end web development (series)</li>
    <li>Intro to basic Python using Turtle graphics (1.5 hrs)</li>
    <li>Build a simple Android app (2 hrs)</li>
    <li>Tech Panels (Q&A, Myths/Lessons learned in tech, Salary Negotiation, Interview Prep, etc)</li>
</ul>

<h3>Eligibility</h3>

If you identify as a minority in tech, you are welcome to join Habibi Code. This includes, but is not limited to: women, BIPOC, the Latinx community, LGBTQ+ individuals, the formerly incarcerated community, those from low socio-economic backgrounds, those with non-traditional educational backgrounds. 

<h3>Join</h3>
<a href="https://discord.gg/f9JwpyvhqP">Here is a link to join our Discord community!</a> Please note that you will not have full access to Habibi Code once you join - in order to maintain the purposeful environment we are fostering, we ask that anyone interested in joining Habibi Code fill out a form confirming their identity as a minority in tech. If you meet the eligibility criteria, you will receive access to our full Discord community.  We are also collecting optional demographic information to better understand our community makeup.
